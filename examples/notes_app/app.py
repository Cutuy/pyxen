"""notes_app — a small FastAPI notes service that uses the runtime.

This is a plain web app: no agents, no LLM calls. The point is to
demonstrate that the runtime serves a normal Python web app just as well
as an agent-containing one.

Run with:

    pip install pyxen[examples]      # adds fastapi + uvicorn
    pyxen validate runtime.json
    uvicorn examples.notes_app.app:app --reload

Endpoints:

    POST /notes        — create a note (auth via runtime.identity)
    GET  /notes        — list all notes
    GET  /notes/{id}   — fetch a single note
    DELETE /notes/{id} — remove a note
    GET  /healthz      — liveness probe (no runtime)
"""

from __future__ import annotations

import os
import uuid
from contextlib import asynccontextmanager
from datetime import UTC, datetime
from typing import Any

from fastapi import FastAPI, HTTPException
from fastapi.responses import HTMLResponse

from pyxen import Runtime

RUNTIME_PATH = os.environ.get("PYXEN_RUNTIME", "runtime.json")


@asynccontextmanager
async def lifespan(app: FastAPI) -> Any:
    """Load the runtime on startup; close nothing (runtime is stateless)."""
    rt = await Runtime.load(RUNTIME_PATH)
    app.state.runtime = rt
    yield


app = FastAPI(
    title="pyxen notes",
    version="0.1.0",
    description="A small notes service. The interesting bit is that every environment-shaped concern goes through the runtime.",
    lifespan=lifespan,
)


def _now_iso() -> str:
    return datetime.now(UTC).isoformat()


def _rt() -> Runtime:
    rt: Runtime | None = getattr(app.state, "runtime", None)
    if rt is None:
        raise HTTPException(status_code=503, detail="runtime not loaded")
    return rt


