@app.get("/healthz")
async def healthz() -> dict[str, str]:
    return {"status": "ok"}


@app.post("/notes")
async def create_note(body: dict[str, Any]) -> dict[str, Any]:
    rt = _rt()
    text = body.get("text")
    if not isinstance(text, str) or not text.strip():
        raise HTTPException(status_code=400, detail="text is required")

    async with rt.observability.trace("create_note") as span:
        me = await rt.identity.current()
        span.set_attribute("author", me.id)

        # Reject anonymous in any non-dev environment
        if me.id == "anonymous" and os.environ.get("PYXEN_ALLOW_ANON") != "1":
            raise HTTPException(status_code=401, detail="authentication required")

        # Soft budget check before writing
        check = await rt.tokens.check("notes-write", 100)
        if not check.allowed:
            raise HTTPException(status_code=429, detail=check.reason or "rate limited")

        note_id = str(uuid.uuid4())
        note = {
            "id": note_id,
            "text": text,
            "author": me.id,
            "created_at": _now_iso(),
        }
        await rt.storage.put("notes", note_id, note)
        await rt.tokens.charge("notes-write", tokens=100, dollars=0.0)
        return note


@app.get("/notes")
async def list_notes() -> list[dict[str, Any]]:
    rt = _rt()
    notes: list[dict[str, Any]] = await rt.storage.query("notes")
    return notes


@app.get("/notes/{note_id}")
async def get_note(note_id: str) -> dict[str, Any]:
    rt = _rt()
    note = await rt.storage.get("notes", note_id)
    if note is None:
        raise HTTPException(status_code=404, detail="note not found")
    return note or {}


@app.delete("/notes/{note_id}", status_code=204)
async def delete_note(note_id: str) -> None:
    rt = _rt()
    deleted = await rt.storage.delete("notes", note_id)
    if not deleted:
        raise HTTPException(status_code=404, detail="note not found")


def _main() -> None:
    """Smoke test: import the app, exercise the endpoints in-process."""
    import asyncio
    import os
    import tempfile
    from pathlib import Path

    # Use a temp runtime.json so the test is hermetic
    with tempfile.TemporaryDirectory() as tmp:
        os.chdir(tmp)
        manifest = {
            "version": "1",
            "identity": {"implementation": "env", "config": {}},
            "tokens": {"implementation": "json_budget", "config": {"path": str(Path(tmp) / "b.json")}},
            "ipc": {"implementation": "inproc", "config": {}},
            "pkg": {"implementation": "dry_run", "config": {}},
            "storage": {"implementation": "inmemory", "config": {}},
            "secrets": {"implementation": "dotenv", "config": {"path": str(Path(tmp) / ".env")}},
            "observability": {"implementation": "null", "config": {}},
        }
        Path("runtime.json").write_text(str(manifest).replace("'", '"'))

        os.environ["PYXEN_IDENTITY_ID"] = "test-user"
        os.environ["PYXEN_ALLOW_ANON"] = "1"

        async def go() -> None:
            # Use FastAPI's TestClient via httpx for in-process requests
            try:
                import httpx  # noqa: F401
            except ImportError:
                # No httpx — fall back to a basic smoke test
                rt = await Runtime.load("runtime.json")
                me = await rt.identity.current()
                assert me.id == "test-user"
                await rt.storage.put("notes", "test", {"id": "test", "text": "hi"})
                note = await rt.storage.get("notes", "test")
                assert note is not None
                assert note["text"] == "hi"
                return

            from fastapi.testclient import TestClient

            with TestClient(app) as client:
                # The lifespan loads the runtime; create a note
                r = client.post("/notes", json={"text": "hello runtime"})
                assert r.status_code == 200, r.text
                note = r.json()
                assert "id" in note
                assert note["text"] == "hello runtime"
                assert note["author"] == "test-user"

                # List notes
                r = client.get("/notes")
                assert r.status_code == 200
                assert len(r.json()) == 1

                # Get a single note
                r = client.get(f"/notes/{note['id']}")
                assert r.status_code == 200
                assert r.json()["text"] == "hello runtime"

                # Delete it
                r = client.delete(f"/notes/{note['id']}")
                assert r.status_code == 204

                # Now it's gone
                r = client.get(f"/notes/{note['id']}")
                assert r.status_code == 404

        asyncio.run(go())


if __name__ == "__main__":
    _main()
